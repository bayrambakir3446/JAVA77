        short s = 20000;
        int i = 2000000000;
        long l = 2000000000000l;
        System.out.println(sayi);
        System.out.println("s = " + s);
        int a, b, c;
        a = 10;
        b = 20;
        c = 30;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
