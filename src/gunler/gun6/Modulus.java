package gunler.gun6;

public class Modulus {
    public static void main(String[] args) {
        // 10 u 3  e boldugumuz de kalan 1
        System.out.println("10 % 3 = " + 10 % 3);
        System.out.println("44 % 2 = " + 44 % 2); // kalan 0 ise bu sayi cift sayidir
        System.out.println("13 % 2 = " + 13 % 2);// kalan 1 ve 13 sayisi tek sayi
        //   %  modulus  veya remainder
        System.out.println("2345%35 = " + 2345 % 35);
        // bir sayinin tek cift oldugunu anlama icin , asal sayilari bulmak icn kullanilabilr


    }
}
